# \n - 줄바꿈
print("안녕하세요.\n저는 뽀로롱입니다.")

# \", \' - 문자열을 감싸는 "", ''를 문자열 안에 입력할 때 역슬러쉬를 이용하여 표현
print('저는 "뽀로롱"입니다.')
print("저는 \"뽀로롱\"입니다.")

# \\ - 문자열에 역슬리쉬를 표현
print("C:\\Users\\Administrator\\Desktop")

# \r - 커서를 맨 앞으로 이동
print("Red Apple\rPine") # Red Apple이 입력된 후 커서를 앞으로 이동하여 뒤에 있는 Pine을 쓴다.

# \b - 백스페이스 ( 한 글자 삭제 )
print("Redd\bApple")

# \t - 탭
print("Red\tApple")
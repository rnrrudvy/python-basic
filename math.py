# abs(절댓값)
print(abs(-5)) # 5

# pow(제곱)
print(pow(3,4)) # 3^4

# max(최댓값)
print(max(5,10)) # 10

# min(최솟값)
print(min(4,7)) # 4

# round(반올림)
print(round(3.14)) # 3
print(round(3.9)) # 4


# math 라이브러리 사용
from math import *

# floor(내림)
print(floor(4.9)) # 4

# ceil(올림)
print(ceil(3.3)) # 4

# sqrt(제곱근)
print(sqrt(16)) # 4